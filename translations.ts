// Helper to expand points from shorthand
const expandPoints = (obj: any) => {
    for (const key in obj) {
        if (typeof obj[key] === 'object' && obj[key] !== null) {
            if (Array.isArray(obj[key].points)) {
                const newPoints: any = {};
                obj[key].points.forEach((point: any, index: number) => {
                    newPoints[index] = point;
                });
                obj[key].points = newPoints;
            }
            expandPoints(obj[key]);
        }
    }
};

